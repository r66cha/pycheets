"""collections — это модуль стандартной библиотеки Python, который расширяет базовые структуры данных (list, dict, set, tuple) более специализированными и производительными вариантами. Его основная цель — дать готовые высокоуровневые структуры, которые решают частые задачи: подсчет элементов, работа с очередями, сохранение порядка, группировка данных и создание словарей с дополнительной логикой. Он особенно важен в задачах алгоритмов, парсинга данных и backend-разработки, потому что позволяет писать меньше кода и избегать ручной реализации типовых структур. Основные компоненты модуля включают Counter для подсчета элементов, defaultdict для словаря с дефолтными значениями, deque для эффективной работы с очередями с обеих сторон, namedtuple для легких структур данных с именованными полями и ChainMap для объединения нескольких словарей в единый интерфейс. В реальной практике collections заменяет множество самописных решений и часто используется для оптимизации производительности при работе с большими объемами данных."""

# ruff: noqa: F401

from collections import Counter, defaultdict, deque, namedtuple, ChainMap
from typing import NamedTuple  # Современный способ

# Counter — подсчет частоты элементов
data = ["a", "b", "a", "c", "b", "a"]
count = Counter(data)
print(count)  # {'a': 3, 'b': 2, 'c': 1}

# defaultdict — словарь с дефолтным значением
graph = defaultdict(list)
graph["a"].append("b")

# deque — двусторонняя очередь
dq = deque([1, 2, 3])
dq.appendleft(0)
dq.append(4)

# namedtuple — структура с именованными полями
Point = namedtuple("Point", ["x", "y"])
p = Point(10, 20)
print(p.x, p.y)


class Point2(NamedTuple):  # Современный способ
    x: int
    y: int


p2 = Point2(1, 2)
print(p2.x, p2.y)


# ChainMap — объединение словарей
a = {"x": 1}
b = {"y": 2}
cm = ChainMap(a, b)
print(cm["x"], cm["y"])

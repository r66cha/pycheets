"""Текст os — один из самых фундаментальных модулей стандартной библиотеки Python, предоставляющий интерфейс для взаимодействия с операционной системой. Через него программа может получать информацию о текущей системе, работать с файлами и директориями, изменять структуру каталогов, читать и изменять переменные окружения, запускать внешние процессы (хотя сейчас для этого чаще используют subprocess), получать информацию о пользователе, правах доступа и путях. Сам модуль не работает с содержимым файлов — для этого используется open() или модуль io. Его задача заключается именно во взаимодействии с операционной системой. Исторически os был основным модулем для работы с путями, однако начиная с Python 3.4 большинство операций с путями рекомендуется выполнять через pathlib, поскольку он предоставляет более удобный объектно-ориентированный интерфейс. Несмотря на это, многие функции os до сих пор активно используются практически в каждом Python-проекте.

Самые важные части модуля можно разделить на несколько групп.

Работа с директориями

* os.getcwd() — получить текущую рабочую директорию.
* os.chdir() — сменить рабочую директорию.
* os.listdir() — получить список файлов и папок.
* os.mkdir() — создать директорию.
* os.makedirs() — создать вложенные директории.
* os.rmdir() — удалить пустую директорию.
* os.removedirs() — удалить цепочку пустых директорий.

Работа с файлами

* os.remove() — удалить файл.
* os.rename() — переименовать файл или папку.
* os.replace() — заменить существующий файл.
* os.stat() — получить информацию о файле.
* os.walk() — рекурсивный обход дерева каталогов.

Работа с путями (os.path)

* os.path.exists() — существует ли путь.
* os.path.isfile() — является ли объект файлом.
* os.path.isdir() — является ли объект директорией.
* os.path.join() — безопасно объединить части пути.
* os.path.abspath() — получить абсолютный путь.
* os.path.basename() — имя файла.
* os.path.dirname() — директория файла.
* os.path.splitext() — разделить имя и расширение.

Переменные окружения

* os.environ — словарь переменных окружения.
* os.getenv() — получить значение переменной.
* os.putenv() — установить переменную окружения.

Информация о системе

* os.name — тип операционной системы.
* os.cpu_count() — количество логических процессоров.
* os.getpid() — PID текущего процесса.

Работа с правами

* os.chmod() — изменить права доступа.
* os.access() — проверить права доступа.

Разделитель путей

* os.sep
* os.linesep
* os.pathsep

На практике чаще всего используют именно os.getcwd(), os.listdir(), os.walk(), os.environ, os.getenv(), os.cpu_count() и некоторые функции из os.path. Остальные применяются значительно реже.
"""

# ruff: noqa: F401

import os

# Текущая директория
print(os.getcwd())
# Список файлов
print(os.listdir())
# Создание папки
os.mkdir("data")
# Проверка существования
print(os.path.exists("data"))
# Объединение путей
path = os.path.join("data", "users.txt")
print(path)
# Получение переменной окружения
print(os.getenv("HOME"))
# Количество процессоров
print(os.cpu_count())
# Рекурсивный обход директорий
for root, dirs, files in os.walk("."):
    print(root, files)

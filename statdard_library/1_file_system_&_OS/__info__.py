"""os — один из самых фундаментальных модулей стандартной библиотеки Python, предоставляющий интерфейс для взаимодействия с операционной системой. Через него программа может получать информацию о текущей системе, работать с файлами и директориями, изменять структуру каталогов, читать и изменять переменные окружения, запускать внешние процессы (хотя сейчас для этого чаще используют subprocess), получать информацию о пользователе, правах доступа и путях. Сам модуль не работает с содержимым файлов — для этого используется open() или модуль io. Его задача заключается именно во взаимодействии с операционной системой. Исторически os был основным модулем для работы с путями, однако начиная с Python 3.4 большинство операций с путями рекомендуется выполнять через pathlib, поскольку он предоставляет более удобный объектно-ориентированный интерфейс. Несмотря на это, многие функции os до сих пор активно используются практически в каждом Python-проекте."""

# ruff: noqa: F401

import os

# Текущая директория
print(os.getcwd())
# Список файлов
print(os.listdir())
# Создание папки
os.mkdir("data")
# Проверка существования
print(os.path.exists("data"))
# Объединение путей
path = os.path.join("data", "users.txt")
print(path)
# Получение переменной окружения
print(os.getenv("HOME"))
# Количество процессоров
print(os.cpu_count())
# Рекурсивный обход директорий
for root, dirs, files in os.walk("."):
    print(root, files)


"""pathlib — это современный модуль стандартной библиотеки Python для работы с путями к файлам и директориям. До его появления для этих задач использовали в основном os.path, где путь представлялся обычной строкой, а все операции выполнялись функциями. pathlib изменил подход: путь стал полноценным объектом (Path), у которого есть методы для проверки существования, создания директорий, чтения и записи файлов, поиска объектов в файловой системе и получения информации о пути. Благодаря этому код становится более читаемым и безопасным, а также автоматически работает на разных операционных системах, поскольку pathlib самостоятельно использует правильные разделители путей (/ в Linux/macOS и \ в Windows). Сегодня при написании нового кода именно pathlib считается рекомендуемым способом работы с файловой системой.
"""

from pathlib import Path

# Создание пути
path = Path("data") / "users.txt"

# Создание директории
path.parent.mkdir(exist_ok=True)

# Запись файла
path.write_text("Hello, World!")

# Чтение файла
text = path.read_text()
print(text)

# Проверка
print(path.exists())
print(path.is_file())

# Информация
print(path.name)
print(path.stem)
print(path.suffix)
print(path.parent)

# Поиск файлов
for file in Path(".").glob("*.py"):
    print(file)


"""shutil (Shell Utilities) — это модуль стандартной библиотеки Python для выполнения высокоуровневых операций с файлами и директориями. Если os предоставляет низкоуровневые функции взаимодействия с операционной системой (создать каталог, удалить файл, переименовать объект), то shutil объединяет эти операции в более удобные инструменты. Его основная задача — копирование, перемещение, удаление целых директорий и создание архивов. Практически всегда, когда нужно скопировать файл, перенести папку или удалить дерево каталогов, используют именно shutil.
"""

import shutil

# Копирование файла
shutil.copy("a.txt", "backup/a.txt")

# Перемещение файла
shutil.move("a.txt", "archive/a.txt")

# Копирование директории
shutil.copytree("project", "project_backup")

# Удаление директории со всем содержимым
shutil.rmtree("project_backup")

# Информация о диске
usage = shutil.disk_usage("/")
print(f"Всего: {usage.total}")
print(f"Свободно: {usage.free}")

# Создание ZIP-архива
shutil.make_archive("backup", "zip", "project")


"""tempfile — это модуль стандартной библиотеки Python для безопасного создания временных файлов и директорий. Его основная задача — предоставить место для хранения данных, которые нужны только во время работы программы. После завершения работы такие файлы обычно автоматически удаляются. Это избавляет от необходимости самостоятельно придумывать уникальные имена файлов, следить за конфликтами имен и очищать временные данные. Модуль особенно полезен при обработке загружаемых файлов, архивов, изображений, промежуточных результатов вычислений, тестировании и работе с внешними программами. Все временные объекты создаются в системной временной директории, которая определяется операционной системой."""

import tempfile

# Временный файл
with tempfile.TemporaryFile(mode="w+t") as file:
    file.write("Hello")
    file.seek(0)
    print(file.read())

# Временный именованный файл
with tempfile.NamedTemporaryFile(mode="w", delete=True) as file:
    print(file.name)

# Временная директория
with tempfile.TemporaryDirectory() as directory:
    print(directory)

# Путь к системной временной директории
print(tempfile.gettempdir())


"""sys — это модуль стандартной библиотеки Python, который предоставляет доступ к внутреннему состоянию интерпретатора Python и текущего процесса. Если os отвечает за взаимодействие с операционной системой, то sys отвечает за взаимодействие с самим Python. Через него можно узнать, как был запущен интерпретатор, какие аргументы ему передали, где он ищет модули, какая используется версия Python, сколько памяти занимает объект, а также завершить выполнение программы. Большинство программ используют лишь небольшую часть возможностей sys, но эти возможности являются очень важными."""


import sys

print("Версия:", sys.version)

print("Платформа:", sys.platform)

print("Аргументы:", sys.argv)

print("Интерпретатор:", sys.executable)

print("Размер списка:", sys.getsizeof([1, 2, 3]))

print("Пути поиска модулей:")

for path in sys.path:
    print(path)
